"""3. Реализовать программу работы с органическими клетками. Необходимо создать класс Клетка. В его конструкторе
инициализировать параметр, соответствующий количеству клеток (целое число).
В классе должны быть реализованы методыперегрузки арифметических операторов:
сложение (__add__()), вычитание (__sub__()), умножение (__mul__()),деление (__truediv__()).

Данные методы должны применяться только к клеткам и выполнять увеличение, уменьшение,умножение и обычное
(не целочисленное) деление клеток, соответственно. В методе деления должно осуществляться
округление значения до целого числа.

Сложение. Объединение двух клеток. При этом число ячеек общей клетки должно равняться сумме ячеек исходных двух клеток.
Вычитание. Участвуют две клетки. Операцию необходимо выполнять только если разность количества ячеек двух клеток больше
нуля, иначе выводить соответствующее сообщение.
Умножение. Создается общая клетка из двух. Число ячеек общей клетки определяется как произведение количества ячеек этих
двух клеток.
Деление. Создается общая клетка из двух. Число ячеек общей клетки определяется как целочисленное деление количества
ячеек этих двух клеток.

В классе необходимо реализовать метод make_order(), принимающий экземпляр класса и количество ячеек в ряду. Данный
метод позволяет организовать ячейки по рядам.

Метод должен возвращать строку вида *****\n*****\n*****..., где количество ячеек между \n равно переданному аргументу.
Если ячеек на формирование ряда не хватает, то в последний ряд записываются все оставшиеся.

Например, количество ячеек клетки равняется 12, количество ячеек в ряду — 5. Тогда метод make_order()
вернет строку: *****\n*****\n**.
Или, количество ячеек клетки равняется 15, количество ячеек в ряду — 5. Тогда метод make_order()
вернет строку: *****\n*****\n*****."""
from math import ceil


class Cell:

    def __init__(self, value):
        self.value = value

    def __add__(self, other):
        return Cell(self.value + other.value)

    def __sub__(self, other):
        if self.value > other.value:
            return Cell(self.value - other.value)
        else:
            raise ValueError('Вычитание невозможно')

    def __mul__(self, other):
        return Cell(self.value * other.value)

    def __truediv__(self, other):
        return Cell(ceil(self.value / other.value))

    def __str__(self):
        return f'{self.value * "*"}\n'

    def make_order(self, row):
        return "\n".join(f'{"*" * row}' for _ in range(self.value // row)) + '\n' + '*' * (self.value % row) + '\n' \
            if self.value > row else "*" * self.value + '\n'


if __name__ == '__main__':
    this_cell = Cell(3)
    another_cell = Cell(22)
    biggest_cell_on_da_block = Cell(54)

    print(unit := this_cell + another_cell)
    print(unit.make_order(7))
    print(this_cell.make_order(5))
    print(biggest_cell_on_da_block.make_order(13))

    print(biggest_cell_on_da_block / another_cell)
    print(this_cell * unit)
    print(biggest_cell_on_da_block - unit)
    print(biggest_cell_on_da_block.make_order(20))
    print(another_cell.make_order(11))
